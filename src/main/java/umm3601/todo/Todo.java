package umm3601.todo;

public class Todo {
  String _id;
  String owner;
  Boolean status;
  String body;
  String category;

  public String get_id() {
    return _id;
  }

  public String getOwner() {
    return owner;
  }

  public Boolean status() {
    return status;
  }

  public String getBody() {
    return body;
  }

  public String getCategory() {
    return category;
  }
}





